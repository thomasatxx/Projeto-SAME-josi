   /**
    * método para atualizar a tabela
    */
    void listar(){
        Oracle con = new Oracle("10.9.1.210","amb","arquivo");//cria a conexão
        con.connect();//estabelece conexão
        ResultSet rs = null;
        try {
            PreparedStatement st = con.c.prepareStatement("select Login,Perfil from USUARIOS");//prepara a query
            rs = st.executeQuery();//executa
            tabela.setModel(DbUtils.resultSetToTableModel(rs));//preenche a tabela com o resultado
            tabela.setEnabled(false);//desabilita a tabela
            tabela.getColumnModel().getColumn(0).setResizable(false);//desabilita a possibilidade de redimensionar a tabela
            tabela.getColumnModel().getColumn(1).setResizable(false);
            tabela.getColumnModel().getColumn(0).setPreferredWidth(130);
            tabela.getColumnModel().getColumn(1).setPreferredWidth(20);
            tabela.getTableHeader().setReorderingAllowed(false);
            tabela.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        } catch (SQLException ex) {
            Logger.getLogger(CadastroUser.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
