    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        tabela.setEnabled(true);//ao clicar em excluir habilita a tabela
        if(!(this.nomeLogin.equals(null)) && tabela.isEnabled()){//se o nome selecionado não for nulo e a tabela estiver habilitada
            int resultado = JOptionPane.showConfirmDialog(null,"Deseja realizar a exclusão de "+this.nomeLogin+" ?"," Alerta! ",JOptionPane.YES_NO_OPTION);//pergunta se deseja excluir
            if(resultado == JOptionPane.YES_OPTION){//se a opção escolhida for SIM
                if(this.nome_usuario.equals(this.nomeLogin)){//mas se o nome de login for igual ao nome selecionado
                    JOptionPane.showMessageDialog(null,"Usuário "+this.nome_usuario+" já está logado!","Alerta!",JOptionPane.INFORMATION_MESSAGE);//msg de bloqueio
                    btnCancelarActionPerformed(evt);//cancela tudo
                }else{
                    new UsuarioCadastro(this.nomeLogin);//se estiver tudo correto, chama o construtor da classe usuario por parametro o nome(unique)
                    this.nomeLogin = null;//seta o nome como nulo para não ficar salvo na memoria 
                    listar();//atualiza a tabela
                }
            }else{
                btnCancelarActionPerformed(evt);
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

